from redeclipse.textures.basetex import SimpleColourTex


class Gray(SimpleColourTex):
    r = 0.5
    g = 0.5
    b = 0.5

class White(SimpleColourTex):
    r = 1
    g = 1
    b = 1

class Black(SimpleColourTex):
    r = 0.1
    g = 0.1
    b = 0.1

class Red(SimpleColourTex):
    r = 1
    g = 0
    b = 0
