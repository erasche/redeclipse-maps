from redeclipse.textures.basetex import SimpleColourTex


class Gray(SimpleColourTex):
    r = 0.5
    g = 0.5
    b = 0.5


class DarkGray(SimpleColourTex):
    r = 0.2
    g = 0.2
    b = 0.2


class White(SimpleColourTex):
    r = 1
    g = 1
    b = 1


class Black(SimpleColourTex):
    r = 0.1
    g = 0.1
    b = 0.1


class Red(SimpleColourTex):
    r = 1
    g = 0
    b = 0


class Green(SimpleColourTex):
    r = 0
    g = 1
    b = 0


class Blue(SimpleColourTex):
    r = 0
    g = 0
    b = 1
